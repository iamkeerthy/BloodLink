    <h2>Our Mission</h2>
    <p>At BloodLink, our mission is to bridge the gap between blood donors and those in need.</p>
    
    <h2>Our Vision</h2>
    <p>We envision a world where blood donation is easy, accessible, and efficient. BloodLink aims to become the leading platform for blood management, ensuring that no one goes without the critical support they need during emergencies.</p>

    <h2>What We Do</h2>
    <p>BloodLink simplifies the blood donation process by providing a platform where donors can easily register, and those in need can request blood. We work closely with hospitals, clinics, and healthcare providers to ensure the blood is delivered to those who need it most.</p>

    <h2>Our Team</h2>
    <p>BloodLink is powered by a passionate team of healthcare professionals, technology experts, and volunteers, all committed to saving lives. Our team is dedicated to making blood donation an easy, stress-free experience for both donors and recipients.</p>

